package basicDay1;

public class DefaultValueOfPrimitiveDatatypes {

	static int a;
	static long b;
	static double c;
	static float d;
	static boolean ge;
	static byte g;

	public static void main(String[] args) {

		System.out.println("Default value of int is: " + a);
		System.out.println("Default value of long: " + b);
		System.out.println("Default value of double: " + c);
		System.out.println("Default value of float: " + d);
		System.out.println("Default value of boolean: " + ge);
		System.out.println("Default value of byte: " + g);

	}

}
